                <h1>Web Development</h1>
                <div id="p">
                    <p>Web development, also known as website development, refers to the tasks associated with creating, building, and maintaining websites and web applications that run online on a browser. It may, however, also include web design, web programming, and database management. Web development is closely related to the job of designing the features and functionality of apps (web design). The term development is usually reserved for the actual construction of these things (that is to say, the programming of sites). The basic tools involved are programming languages called php (Hypertext Markup Language), CSS (Cascading Style Sheets), and JavaScript. There are, however, a number of other programs used to “manage” or facilitate the construction of sites that would otherwise have to be done “from scratch” by writing code. A number of content management systems (CMS) fall into this category, including WordPress, Joomla!, Drupal, TYPO3, and Adobe Experience Manager, among others. There are three main types of web development: front-end development, back-end development, and full-stack development.</p>
                    <p>Front-end development involves the “client-facing” side of web development. That is to say that it generally refers to the portion of the site, app, or digital product that users will see and interact with. A Front-End Developer, therefore, is responsible for the way a digital product looks and “feels,” which is why they are often also referred to as Web Designers. Front-End Developers will usually need to possess a solid understanding of programming languages, including php, CSS, and JavaScript, as well as frameworks like React, Bootstrap, Backbone, AngularJS, and EmberJS.</p>
                    <p>If Front-End Developers are responsible for how a digital product looks, Back-End Developers are focused on how it works. This means they’re responsible for overseeing what's under the hood, including database interactions, user authentication, server, network and hosting configuration, and business logic.
                        The primary responsibility of Back-End Developers is to ensure the functionality of the site, including its responsiveness and speed. To do that, Back-End Developers have to know how to build servers with modern frameworks (while developing custom APIs and serving static websites and files), and how to manage databases and data on a web server. Typically, they use server-side languages, including PHP, Ruby, and Python, as well as tools including MySQL, Oracle, and Git.</p>
                    <p>A Full-Stack Developer is someone familiar with both front- and back-end development. They are generalists, adept at wearing both hats, and familiar with every layer of development. Obviously, employers want to hire Full-Stack Developers - according to an Indeed study, they are the fourth-most in-demand job in tech.</p>
