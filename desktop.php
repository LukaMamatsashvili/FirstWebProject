                    <h2>Python</h2>
                    <p>Python is a high-level, object-oriented programming language that does not need a programmer to write longer lines of code for executing the concept. It is one of the highly flexible languages that one can use. Not only that but Python has a vast range of GUI frameworks that allow developers to create an application in an easy and secure manner. Some of the frameworks that you can use to develop not only windows application but others too with Python, includes, PyQT (a combination of QT library and Python) that lets the developer decide whether to create visual dialogues using QT Designer or create a program using code; Kivy (an OpenGL ES2 framework) enables programmers to write code once and use it on different platforms; WxPython, PyGUI, and Tkinter.</p>
