                    <h2>C#</h2>
                    <p>C# (pronounced as C Sharp) is a modern, type-safe, and object-oriented programming language, designed and developed by Microsoft Corporation. Although having its roots in the C family, it will be familiar to the JAVA and JavaScript programmers. Needless to say, as C# and Windows both are designed by Microsoft, developers can create a windows-based desktop application with ease. C# allows developers to create different types of robust and secure applications that work on the .NET ecosystem.
                       Some of the frameworks that you can use while developing a desktop app using C# includes the .NET WPF ( that works on XAML to offer a declarative application programming model), Uno Platform, Avalonia, .NET MAUI, WinForms, UWP, Xamarian, and Blazor. These frameworks support numerous application development features such as resources, controls, application model, graphics, data binding, security, layout, and data binding. Moreover, with the release of .NET Core, you can use C# for cross-platform development as well. </p>
