                    <h2>JavaScript</h2>
                    <p>Javascript or JS is a prototype-based, interpreted, single-threaded, lightweight dynamic programming language that supports object-oriented, functional programming type. Despite it being well known as a scripting language for web pages, several non-browser environments also use it. It is among the top programming languages for desktop app development. Using frameworks like Buildfire.js, Electron, NW.js, AppJS, Meteor, and Proton Natives, JS can help developers in designing and developing cross-platform desktop applications.</p>
