                    <h2>Pascal</h2>
                    <p>Object Pascal is an extension to the Pascal programming language that supports object-oriented programming features like methods and classes. It is compiled to a fast, type-safe, and native code. Not only that but object pascal can be used to develop apps for Linux, Windows, and macOS at the same time. Two major implementations that are available for object Pascal include Delphi (a commercial Rapid Application Development environment) and Free Pascal(an open-source compiler and run-time library). Some of the major frameworks that you will need while developing desktop applications using Object Pascal are Lazarus, Oxygene, and Firemonkey. </p>
