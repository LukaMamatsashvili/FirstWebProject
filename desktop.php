                    <h2>Java</h2>
                    <p>Java is an object-oriented, class-based programming language that aims to have as low implementation dependencies as possible. It intends to let the programmer write once, run anywhere (WORA). Simply put, whenever a programmer writes code in Java, it is compiled by javac. The result of this compilation is a class file or the bytecode(not a machine native code). To execute this file or bytecode in the machine, Java needs an interpreter - JVM or Java Virtual Machine which converts bytecode into machine language. As JVM depends on the operating system, we get different variations of it that can be downloaded according to the requirement and be used to run code on different operating systems. That being said, it is used to develop robust and platform-independent applications.
                       A few tools that you will need for building a desktop application with Java entails JDK, Netbeans IDE, Java Swing, Java FX, and SQL. Along with that, you will also need some frameworks like Eclipse RCP, JFoenix (JavaFX material), TornadoFX, Spring, Kafka, Grails, DropWizard, GWT, Hadoop, Log4j, Hibernate, JMF, and so on based on the requirements you have. </p>
