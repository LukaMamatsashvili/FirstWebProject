                    <h2>C++</h2>
                    <p>Despite its high entry barrier, C++ is one of the most popular and commonly used programming languages for game designers. It offers a high abstraction level for both fine object-oriented programming and generic programming, providing direct control over hardware and graphical processes. Additionally, C++ allows the optimisation of special parts to a very high level - a feature almost impossible to achieve with other languages. C++ is used to build most big console and Windows games. Apart from that, popular games built using C++ include Assassin's Creed series, Counter-Strike, Football Pro, Starcraft, Master of Orion III. </p>
                    <img src="images/1822px-ISO_C++_Logo.svg.png">
                </div>
                <div>
                    <h2>C#</h2>
                    <p>Despite considered easier than C++, C# is a powerful language. Its XNA framework makes it perfect for building games on Windows and Xbox. Additionally, C#'s game engine Unity3D and Xamarin enables developers to build games on all platforms including iOS, Android, PlayStation and Windows. C# is as popular in the gaming industry due to its efficiency and scalability. Popular games developed using C# include Assassin's Creed series, Pokemon Go and Super Mario Run. </p>
                       <img src="images/c--4.svg">
                    </div>
                    <div>
                        <h2>Java</h2>
                        <p>Given Java's versatility and rich collection of open-source material, it is popularly used for the development of indie and mobile games. It is easy-to-use, is beginner friendly and can write a range of reusable system-agnostic code. When compared with C++, Java offers easy writing, debugging, learning and compiling. Games including the likes of Mission Impossible III, Minecraft, FIFA 11, and Ferrari GT 3: WOrld Track, have all been created using Java. </p>
                           <img src="images/java.png">
                    </div>
                    <div>
                        <h2>JavaScript</h2>
                        <p>JavaScript is one of the best programming languages for interactive online games. It integrates with web technologies including CSS and php, allowing the development of cross-platform mobile games.
                            JavaScript is often used along with php5 since the latter cannot create animation or interactivity. Additionally, contributing to its popularity is JavaScript's humongous online community and large number of frameworks. Some popular games made with JavaScript include Hangman, HexGI, Little Alchemy, Swoop and Polycraft. </p>
                        <img src="images/javascript-logo-8892AEFCAC-seeklogo.com.png">
                    </div>
                <div>
                    <h2>Python</h2>
                    <p>Although not as popular as Java or C++, Python is used for game development. Its PyGame library is developer-friendly, easy to use for building games and enables developers to prototype games quickly. Additionally, just like Java and C++, Python also runs on OOP principles. Its simple learning curve makes Python a go-to option for many game developers. Some popular games built using Python include Battlefield 2, Disney's Toontown Online, Eve Online and Frets on Fire. </p>
